def process_batch_evaluation_response(response: str, subtopics_batch: List[Dict]) -> List[Dict]:
    """
    处理批次评估的LLM响应
    
    Args:
        response: LLM的响应文本
        subtopics_batch: 对应的子主题批次数据
        
    Returns:
        List[Dict]: 处理后的评估结果
    """
    try:
        # 解析JSON响应
        json_begin = response.find("{")
        json_end = response.rfind("}")
        if json_begin != -1 and json_end != -1:
            evaluation_data = json.loads(response[json_begin:json_end + 1])
            
            # 验证响应格式
            if "evaluations" in evaluation_data and isinstance(evaluation_data["evaluations"], list):
                results = []
                for eval_item in evaluation_data["evaluations"]:
                    if all(key in eval_item for key in ["subtopic_index", "score", "reason"]):
                        # 找到对应的原始子主题数据
                        original_subtopic = None
                        for st in subtopics_batch:
                            if st["batch_index"] == eval_item["subtopic_index"]:
                                original_subtopic = st
                                break
                        
                        if original_subtopic:
                            result = {
                                "subtopic_index": eval_item["subtopic_index"],
                                "original_index": original_subtopic["original_index"],
                                "cluster_phrases": original_subtopic["cluster_phrases"],
                                "paper_count": len(original_subtopic["cluster_paper_list"]),
                                "score": eval_item["score"],
                                "reason": eval_item["reason"]
                            }
                            results.append(result)
                
                return results
            else:
                print("Warning: Invalid response format from LLM")
                return []
        else:
            print("Warning: Could not find JSON in LLM response")
            return []
            
    except Exception as e:
        print(f"Error processing batch evaluation response: {e}")
        return []

def evaluate_subtopics_with_full_info(subtopics_data: List[Dict]) -> List[Dict]:
    """
    使用完整信息批次评估所有子主题
    
    Args:
        subtopics_data: 所有子主题数据（包含完整论文信息）
        
    Returns:
        List[Dict]: 所有评估结果
    """
    all_evaluation_results = []
    
    # 准备批次数据
    batches = []
    for i in range(0, len(subtopics_data), BATCH_SIZE):
        batch_data = []
        for j, subtopic in enumerate(subtopics_data[i:i+BATCH_SIZE]):
            batch_item = subtopic.copy()
            batch_item["batch_index"] = i + j  # 批次内的索引
            batch_item["original_index"] = i + j  # 原始索引
            batch_data.append(batch_item)
        batches.append(batch_data)
    
    print(f"总共 {len(batches)} 个批次，每批 {BATCH_SIZE} 个聚类族")
    print(f"使用完整论文信息（无长度限制）")
    
    # 处理每个批次
    for batch_idx, batch in enumerate(tqdm(batches, desc="处理批次")):
        if VERBOSE:
            print(f"\n处理批次 {batch_idx + 1}/{len(batches)}")
            print(f"批次包含聚类族: {[item['batch_index'] for item in batch]}")
            # 显示批次信息统计
            total_papers = sum(len(item['cluster_paper_list']) for item in batch)
            print(f"批次总论文数: {total_papers}")
        
        try:
            # 创建完整信息的批次prompt
            prompt = create_full_info_evaluation_prompt(batch)
            
            if VERBOSE:
                print(f"Prompt长度: {len(prompt)} 字符")
                # 估计token数量（粗略估算）
                estimated_tokens = len(prompt) // 4  # 常见估算：1 token ≈ 4字符
                print(f"估计token数量: ~{estimated_tokens}")
            
            # 调用LLM
            response = llm_call(prompt, LLM_MODEL)
            
            if VERBOSE:
                print(f"响应长度: {len(response)} 字符")
            
            # 处理响应
            batch_results = process_batch_evaluation_response(response, batch)
            
            if batch_results:
                all_evaluation_results.extend(batch_results)
                if VERBOSE:
                    print(f"批次 {batch_idx + 1} 成功评估 {len(batch_results)} 个聚类族")
            else:
                print(f"警告: 批次 {batch_idx + 1} 未获得有效评估结果")
                # 为失败的批次创建默认结果
                for item in batch:
                    all_evaluation_results.append({
                        "subtopic_index": item["batch_index"],
                        "original_index": item["original_index"],
                        "cluster_phrases": item["cluster_phrases"],
                        "paper_count": len(item["cluster_paper_list"]),
                        "score": -1,
                        "reason": "Batch evaluation failed - no valid response"
                    })
            
            # 批次间延迟
            if batch_idx < len(batches) - 1:  # 不是最后一个批次
                time.sleep(BATCH_DELAY)
                
        except Exception as e:
            print(f"处理批次 {batch_idx + 1} 时出错: {e}")
            # 为出错的批次创建默认结果
            for item in batch:
                all_evaluation_results.append({
                    "subtopic_index": item["batch_index"],
                    "original_index": item["original_index"],
                    "cluster_phrases": item["cluster_phrases"],
                    "paper_count": len(item["cluster_paper_list"]),
                    "score": -1,
                    "reason": f"Error: {str(e)}"
                })
    
    return all_evaluation_results

def calculate_comprehensive_statistics(evaluation_results: List[Dict]) -> Dict[str, Any]:
    """
    计算全面的评估统计信息
    
    Args:
        evaluation_results: 所有评估结果
        
    Returns:
        Dict: 统计信息
    """
    valid_scores = [result["score"] for result in evaluation_results if result["score"] > 0]
    failed_evaluations = len([result for result in evaluation_results if result["score"] <= 0])
    
    if not valid_scores:
        return {
            "total_subtopics": len(evaluation_results),
            "valid_evaluations": 0,
            "failed_evaluations": failed_evaluations,
            "average_score": 0,
            "score_distribution": {},
            "quality_breakdown": {}
        }
    
    # 基础统计
    statistics = {
        "total_subtopics": len(evaluation_results),
        "valid_evaluations": len(valid_scores),
        "failed_evaluations": failed_evaluations,
        "success_rate": len(valid_scores) / len(evaluation_results),
        "average_score": sum(valid_scores) / len(valid_scores),
        "score_std": (sum((x - sum(valid_scores) / len(valid_scores)) ** 2 for x in valid_scores) / len(valid_scores)) ** 0.5,
        "min_score": min(valid_scores),
        "max_score": max(valid_scores),
        "score_distribution": {
            "5_excellent": len([s for s in valid_scores if s == 5]),
            "4_good": len([s for s in valid_scores if s == 4]),
            "3_fair": len([s for s in valid_scores if s == 3]),
            "2_poor": len([s for s in valid_scores if s == 2]),
            "1_very_poor": len([s for s in valid_scores if s == 1]),
        }
    }
    
    # 质量分类
    statistics["quality_breakdown"] = {
        "high_quality": statistics["score_distribution"]["5_excellent"] + statistics["score_distribution"]["4_good"],
        "medium_quality": statistics["score_distribution"]["3_fair"],
        "low_quality": statistics["score_distribution"]["2_poor"] + statistics["score_distribution"]["1_very_poor"]
    }
    
    # 计算百分比
    statistics["quality_percentages"] = {
        "high_quality_pct": statistics["quality_breakdown"]["high_quality"] / len(valid_scores) * 100,
        "medium_quality_pct": statistics["quality_breakdown"]["medium_quality"] / len(valid_scores) * 100,
        "low_quality_pct": statistics["quality_breakdown"]["low_quality"] / len(valid_scores) * 100
    }
    
    return statistics

def analyze_clustering_patterns(evaluation_results: List[Dict], subtopics_data: List[Dict]) -> Dict[str, Any]:
    """
    分析聚类模式
    
    Args:
        evaluation_results: 评估结果
        subtopics_data: 原始子主题数据
        
    Returns:
        Dict: 聚类模式分析
    """
    # 按得分分组
    high_quality = [r for r in evaluation_results if r["score"] >= 4]
    medium_quality = [r for r in evaluation_results if r["score"] == 3]
    low_quality = [r for r in evaluation_results if r["score"] <= 2]
    
    # 分析高质量聚类的特征
    high_quality_features = {
        "avg_phrase_count": sum(len(r["cluster_phrases"]) for r in high_quality) / len(high_quality) if high_quality else 0,
        "avg_paper_count": sum(r["paper_count"] for r in high_quality) / len(high_quality) if high_quality else 0,
        "common_phrase_patterns": []  # 可以进一步分析短语模式
    }
    
    # 分析低质量聚类的问题
    low_quality_issues = {
        "common_reasons": [],
        "avg_phrase_count": sum(len(r["cluster_phrases"]) for r in low_quality) / len(low_quality) if low_quality else 0,
        "avg_paper_count": sum(r["paper_count"] for r in low_quality) / len(low_quality) if low_quality else 0
    }
    
    return {
        "high_quality_clusters": len(high_quality),
        "medium_quality_clusters": len(medium_quality),
        "low_quality_clusters": len(low_quality),
        "high_quality_features": high_quality_features,
        "low_quality_issues": low_quality_issues
    }

